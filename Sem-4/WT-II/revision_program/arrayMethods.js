const arr=[1,2,3,4,5,6,7,8];
console.log("actual array")
console.log(arr)

console.log("after pushing")
arr.push(9)
console.log(arr)

console.log("after poped:")
arr.pop()
console.log(arr)

console.log("after shift")
arr.shift()
